                <p>- Tuy vậy, có lẽ do chưa quen mắt nên áo đấu mới nhận về không ít ý kiến trái chiều từ phía các fan của T1. Thậm chí có fan còn cho rằng, Nike đã khá “lười biếng” khi họ gần như mang toàn bộ thiết kế của Rare Atom, thay đổi chút đỉnh rồi trở thành áo đấu mới của T1. Thậm chí có người còn chơi “hệ tâm linh” khi liên tưởng màu sắc logo trên áo với màu sắc của logo CKTG và nghĩ đây “không thể là sự trùng hợp”.</p>

