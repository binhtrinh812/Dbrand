                <h2 id="id_no_bullets_3">III. Bản lĩnh trở lại giúp Faker và đồng đội giành lấy Summoner Cup</h2>

                <p>- Tiến vào vòng trong, T1 nhận được tin chẳng lành khi lần lượt Gen.G Esports và KT Rolster bại trận trước những đội tuyển thuộc khu vực LPL. Khi này, “Quỷ vương” và những đồng đội đứng trước áp lực rất lớn khi họ là đội tuyển Hàn Quốc duy nhất có mặt ở vòng trong. Chiến thắng trước đối thủ Trung Quốc không chỉ để giành lấy Summoner Cup mà còn là vì danh dự, vì niềm tự hào của LCK, một trong những khu vực lớn nhất thế giới. Và rồi, “LCK Last Hope” đã làm được điều đó.</p>

                <p>
                    <img class="img_news" src="./image/news/img-news-detail (7).png" alt="">
                </p>

                <p>- Tiếp đón LNG Esports tại trận Tứ kết, T1 dễ dàng huỷ diệt với tỉ số 3 – 0. Đội tuyển trình diễn lối đánh chắc chắn, không hoa mỹ nhưng hiệu quả. Bên cạnh đó, những lựa chọn lạ như Nilah của Gumayusi hay Senna của Keria đã khiến đối thủ bất ngờ, qua đó giúp T1 lăn cầu tuyết tốt hơn.</p>
                <p>- Tại bán kết, trận đấu giữa T1 và JDG Gaming được coi là “chung kết sớm” của mùa giải năm nay. Mặc dù được đánh giá cao hơn về đội hình, thế nhưng JDG vẫn không thể ngăn cản một T1 với phong độ quá cao, qua đó chịu trận với tỉ số 1 – 3. Ấn tượng nhất trong trận đấu chắc chắn không thể bỏ qua tình huống “Shurima Shuffle” xạ thủ Ruler của Faker, qua đó mở toang chiến thắng cho T1 dù đang bị dẫn trước.</p>
                <p>- Đến với trận đấu cuối cùng, tuyển tiếp đón Weibo Gaming, đối thủ được đánh giá rất cao với đầu tàu là “Top God” The Shy. Song, anh và các đồng đội không thể chống lại sức mạnh quá lớn của một T1 đang trên đà phong độ, qua đó chịu trận với thất bại 0 – 3 chung cuộc.</p>
                <!-- IV  -->
                <h2 id="id_no_bullets_4">IV. “Khắc Tinh LPL” và cách T1 chinh phục Chung Kết Thế Giới 2023</h2>

                <p>- Chức vô địch của T1 có thể được xem là sự kết hợp hoàn hảo của thiên thời, địa lợi và nhân hòa. Thứ nhất, bể tướng rộng của Gumayusi và Keria cho phép đường dưới luôn có lợi thế và sẵn sàng lăn cầu tuyết bất cứ khi nào.</p>
                <p>- Thứ hai, Zeus và Oner đã trưởng thành hơn, trình độ có thể so sánh với các tuyển thủ hàng đầu thế giới. Zeus nay đã biết tạo lợi thế khi đi đường, thậm chí có thể solo-kill được một The Shy đang trong phong độ rất cao. Oner cũng đã tự tin chơi những vị tướng carry như Lee Sin hay Nocturne và phát huy vai trò quan trọng trong những pha giao tranh chủ chốt.</p>
                <p>
                    <img class="img_news" src="./image/news/img-news-detail (8).jpg" alt="">
                </p>
                <p>- Thứ ba, Faker đã thực sự “hồi xuân”. Meta thuận lợi cho phép anh chơi những pháp sư kiểm soát như Orianna, Azir hay Ahri. Kể cả khi cần thiết, “Quỷ Vương” sẵn sàng tung ra những chất tướng carry như Akali hay Sylas.</p>
                <p>- Những yếu tố bên ngoài cũng đóng vai trò rất quan trọng tạo nên thành công của T1 tại mùa giải năm nay. Họ được chơi trên sân nhà Hàn Quốc và có lực lượng fan hùng hậu từ khắp nơi trên thế giới. Thêm vào đó, T1 chưa từng thua bất cứ đội tuyển Trung Quốc nào trong một cặp Bo5 (Best of 5) tại một giải đấu quốc tế. Đó là lý do vì sao đội tuyển được cộng đồng Liên Minh Huyền Thoại gọi vui với cái tên “LPL Slayer” (tạm dịch: Khắc Tinh LPL).</p>
                <p>- Chung Kết Thế Giới 2023 đánh dấu chức vô địch của T1 sau 7 năm chờ đợi ngay trên quê nhà Hàn Quốc. Bản thân Faker cũng đã sở hữu chiếc cúp thứ tư trong sự nghiệp sau những năm 2013, 2015 và 2016. Trên hết, chiến thắng của T1 đã giúp những “Tcon” gạt bỏ đi tất cả nghi ngờ trong suốt một năm vừa qua. Khu vực LCK sẽ tiếp tục là trụ cột, là hạt nhân thúc đẩy sự phát triển của Liên Minh Huyền Thoại trên phạm vi toàn thế giới.</p>
