                <h2 id="id_no_bullets_2">II. Nghi ngờ đến hơi thở cuối cùng</h2>

                <p>- Đó là câu nói vui của những người hâm mộ T1 dành cho đội tuyển mà mình yêu thích. Thế nhưng, sự nghi ngờ là có lý do vì T1 thể hiện phong độ thiếu ổn định kể từ suốt giải đấu LCK Mùa Hè vừa qua. Thậm chí, trước một đối thủ được đánh giá là yếu hơn trong trận chung kết, Faker và đồng đội tiếp tục bị đặt những dấu hỏi lớn. Cũng tại trận đấu cuối cùng của Chung Kết Thế Giới 2022, T1 đã gục ngã trước DRX, một đội tuyển được xếp hạng seed 4 của khu vực Hàn Quốc.</p>
                <p>Khi “Quỷ Vương” trở lại, phong độ của T1 cũng không còn đạt đỉnh như trước nữa. Đội tuyển thua trắng 0 – 3 trong trận chung kết tổng, qua đó để lỡ ngôi vương vô địch từ tay Gen.G Esports. Rõ ràng, lực lượng của T1 khó lòng so sánh được với Chovy, Peanut hay hàng loạt những tuyển thủ chất lượng từ khu vực LPL. Đến với Chung Kết Thế Giới 2023, màn trình diễn của Faker và đồng đội tại Vòng Thể thức Thuỵ Sĩ (Swiss Stage) cũng khiến người hâm mộ đặt ra nhiều hoài nghi. Sau trận thắng có phần vất vả trước Team Liquid, T1 thua toàn diện trước Gen.G Esports và phải nhờ hai chiến thắng trước Cloud 9 và Bilibili Gaming để có vé vào vòng trong.</p>
                <p>
                    <img class="img_news" src="./image/news/img-news-detail (6).jpg" alt="">
                </p>

                <p>- Hành trình của T1 tại mùa giải năm nay chứng kiến biết bao sự thăng trầm. Khi mà giải đấu LCK Mùa Hè mới chỉ khởi tranh được 4 tuần, Faker phải nghỉ thi đấu tạm thời do chấn thương tay. Thiếu đi đầu tàu quan trọng, T1 trải qua chuỗi ngày thi đấu tệ hại khi chỉ thắng 1 trong 6 trận sau đó. Tuyển thậm chí còn thất bại trước những đối thủ được đánh giá thấp hơn nhiều như BRION hay Nongshim RedForce.</p>

