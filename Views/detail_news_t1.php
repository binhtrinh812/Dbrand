                <h2 id="id_no_bullets_1">I. Áo đấu với logo "hoàng kim" đầy mới mẻ, tựa như một bộ Long Bào</h2>

                <p>- Đây cũng được xem là một trang phục phá cách của T1. Lần đầu tiên trong lịch sử, đội tuyển giàu thành tích bậc nhất CKTG sẽ khoác lên mình chiếc áo full trắng với logo màu vàng ánh kim trước ngực. Tên của các nhà tài trợ được in trên tay áo và cầu vai trong khi chữ “SKT 5GX” được gắn ngay phía trên logo. Chính giữa chữ “SKT 5GX” và logo T1 chính là 3 ngôi sao tượng trưng cho 3 lần lên ngôi CKTG của đội tuyển này. Từ trước đến nay, T1 (hay trước đây là SKT T1) vốn đã vô cùng quen thuộc với áo đấu tone đỏ - trắng hoặc trắng - đen (trắng - xanh ở mùa 5) và chưa bao giờ áo đấu của đội tuyển này chỉ có một màu chủ đạo như CKTG sắp tới.</p>

                <p>
                    <img class="img_news" src="./image/news/img-news-detail (5).jpg" alt="">
